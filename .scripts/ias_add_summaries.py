def parse_frontmatter(text: str) -> tuple[dict[str, str], str] | None:
    if not text.startswith("---\n"):
        return None
    end = text.find("\n---\n", 4)
    if end == -1:
        return None
    fields: dict[str, str] = {}
    for line in text[4:end].splitlines():
        if ":" in line:
            k, v = line.split(":", 1)
            fields[k.strip()] = v.strip()
    return fields, text[end + 5 :]


def serialize_frontmatter(fields: dict[str, str]) -> str:
    order = ["tags", "standard", "title", "effective", "source", "key"]
    lines: list[str] = []
    used: set[str] = set()
    for k in order:
        if k in fields:
            lines.append(f"{k}: {fields[k]}")
            used.add(k)
    for k, v in fields.items():
        if k not in used:
            lines.append(f"{k}: {v}")
    return "---\n" + "\n".join(lines) + "\n---\n\n"


def add_summary(path: Path, meta: dict) -> bool:
    text = path.read_text(encoding="utf-8")
    if "## 📌 中文提炼" in text:
        print(f"  跳过（已有提炼）: {path.name}")
        return False

    parsed = parse_frontmatter(text)
    if not parsed:
        print(f"  跳过（无 frontmatter）: {path.name}")
        return False

    fields, body = parsed
    if meta["key"]:
        fields["key"] = "true"

    header = serialize_frontmatter(fields)
    summary = f"{meta['h1']}\n\n## 📌 中文提炼\n\n{meta['body'].strip()}\n\n---\n\n"
    path.write_text(header + summary + body.lstrip("\n"), encoding="utf-8")
    print(f"  已添加: {path.name}")
    return True


def main() -> None:
    count = 0
    for filename, meta in SUMMARIES.items():
        path = IAS_DIR / filename
        if not path.exists():
            print(f"  缺失文件: {filename}")
            continue
        if add_summary(path, meta):
            count += 1
    print(f"\n完成：添加 {count} 个中文提炼")


if __name__ == "__main__":
    main()
